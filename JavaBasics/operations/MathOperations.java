package operations;

public class MathOperations {
    public static void main(String[] args) {
        int a = 7;
        int b = 5;

        int sum = a + b; // 12
        int subtraction = a - b; // 2
        int multiplication = a * b; // 35

        int quocient = a / b; // 1
        int reminder = a % b; // 2
        float division = (float) a / b; // 1.4

        int i = 1;
        int post_increment = i++; // post_increment = 1, i = 2
        int pre_increment = ++i; // pre_increment = 3, i = 3
    }
}
